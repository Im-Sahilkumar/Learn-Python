# Assignment operators
x = 5
x += 3  # Equivalent to x = x + 3
print(x)  # Output: 8   
x -= 2  # Equivalent to x = x - 2
print(x)  # Output: 6
x *= 4  # Equivalent to x = x * 4
print(x)  # Output: 24
x /= 6  # Equivalent to x = x / 6
print(x)  # Output: 4.0
x //= 2  # Equivalent to x = x // 2
print(x)  # Output: 2.0
x %= 3  # Equivalent to x = x % 3
print(x)  # Output: 2.0
x **= 2  # Equivalent to x = x ** 2
print(x)  # Output: 4.0
